static uint8_t OLED_GRAM[128][8];
static uint8_t OLED_dirty = 0xFF;
/* 每页脏列范围: 只刷 [col_min, col_max] 这段, 避免整页 128 列重发 */
static uint8_t OLED_col_min[8];
static uint8_t OLED_col_max[8];

/* 标记某页某列为脏, 并更新该页脏列区间 */
static void OLED_MarkDirty(uint8_t page, uint8_t col) {
    if (page >= 8 || col >= 128) return;
    OLED_dirty |= (1 << page);
    if (col < OLED_col_min[page]) OLED_col_min[page] = col;
    if (col > OLED_col_max[page]) OLED_col_max[page] = col;
}

