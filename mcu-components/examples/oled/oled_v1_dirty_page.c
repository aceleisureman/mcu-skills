typedef struct {
    GPIO_TypeDef *scl_port;
    uint16_t scl_pin;
    GPIO_TypeDef *sda_port;
    uint16_t sda_pin;
} OLED_Bus;

static const OLED_Bus OLED_BUSES[] = {
    {OLED_SCL_PORT, OLED_SCL_PIN, OLED_SDA_PORT, OLED_SDA_PIN},
};

static const uint8_t OLED_ADDR_LIST[] = {0x78};  // 只使用一个地址测试

/* 直接写 BSRR 寄存器: 低16位置位, 高16位复位, 比 HAL_GPIO_WritePin 快很多 */
static inline void SCL_High(const OLED_Bus *bus) { bus->scl_port->BSRR = bus->scl_pin; }
static inline void SCL_Low(const OLED_Bus *bus)  { bus->scl_port->BSRR = (uint32_t)bus->scl_pin << 16; }
static inline void SDA_High(const OLED_Bus *bus) { bus->sda_port->BSRR = bus->sda_pin; }
static inline void SDA_Low(const OLED_Bus *bus)  { bus->sda_port->BSRR = (uint32_t)bus->sda_pin << 16; }

/* 64MHz 下 60 次循环约 3.6us, SCL 半周期 ~140kHz, 远低于 SSD1306 上限 400kHz */
static void IIC_Delay(void) {
    for (volatile uint8_t i = 0; i < 60; i++) {
        __NOP();
    }
}

static void IIC_Start(const OLED_Bus *bus) {
    SDA_High(bus);
    SCL_High(bus);
    IIC_Delay();
    SDA_Low(bus);
    IIC_Delay();
    SCL_Low(bus);
    IIC_Delay();
}

static void IIC_Stop(const OLED_Bus *bus) {
    SDA_Low(bus);
    SCL_High(bus);
    IIC_Delay();
    SDA_High(bus);
    IIC_Delay();
}

/* 第 9 个 SCL 时钟供从机应答; SDA 全程保持推挽输出, 不再切换输入模式
 * (省去每字节两次 HAL_GPIO_Init 调用, 大幅提升刷新吞吐) */
static void IIC_WaitAck(const OLED_Bus *bus) {
    SDA_High(bus);
    IIC_Delay();
    SCL_High(bus);
    IIC_Delay();
    SCL_Low(bus);
}

static void IIC_WriteByte(const OLED_Bus *bus, uint8_t data) {
    for (uint8_t i = 0; i < 8; i++) {
        if (data & 0x80) SDA_High(bus);
        else SDA_Low(bus);
        data <<= 1;
        SCL_High(bus);
        IIC_Delay();
        SCL_Low(bus);
        IIC_Delay();
    }
    IIC_WaitAck(bus);
}

static void OLED_WriteCmdTo(const OLED_Bus *bus, uint8_t addr, uint8_t cmd) {
    IIC_Start(bus);
    IIC_WriteByte(bus, addr);
    IIC_WriteByte(bus, 0x00);
    IIC_WriteByte(bus, cmd);
    IIC_Stop(bus);
}

static void OLED_WriteCmd(uint8_t cmd) {
    for (uint8_t b = 0; b < sizeof(OLED_BUSES) / sizeof(OLED_BUSES[0]); b++) {
        for (uint8_t a = 0; a < sizeof(OLED_ADDR_LIST) / sizeof(OLED_ADDR_LIST[0]); a++) {
            OLED_WriteCmdTo(&OLED_BUSES[b], OLED_ADDR_LIST[a], cmd);
        }
    }
}

